// 递归宽化：string叶子→string，函数保留原类型，对象递归
type DeepLoosen<T> = {
  [K in keyof T]: T[K] extends (...args: infer A) => infer R
    ? (...args: A) => R
    : T[K] extends string
      ? string
      : DeepLoosen<T[K]>;
};

export type Translations = DeepLoosen<typeof zh>;
